package com.app.jsonapp.model;

public class AccountInfo 
{
	private String name;
	private String decodedString;
	private String trimmedString;
	private String incrimentalString;
	private String uppercaseString;
	private String reverseString;
	private int amount;

	public String getName() 
	{
		return name;
	}

	public void setName(final String name) 
	{
		this.name = name;
	}

	public String getDecodedString() 
	{
		return decodedString;
	}

	public void setDecodedString(final String decodedString) 
	{
		this.decodedString = decodedString;
	}

	public String getTrimmedString() 
	{
		return trimmedString;
	}

	public void setTrimmedString(final String trimmedString) 
	{
		this.trimmedString = trimmedString;
	}

	public String getIncrimentalString() 
	{
		return incrimentalString;
	}

	public void setIncrimentalString(final String incrimentalString) 
	{
		this.incrimentalString = incrimentalString;
	}

	public String getUppercaseString() 
	{
		return uppercaseString;
	}

	public void setUppercaseString(final String uppercaseString) 
	{
		this.uppercaseString = uppercaseString;
	}

	public String getReverseString() 
	{
		return reverseString;
	}

	public void setReverseString(final String reverseString) 
	{
		this.reverseString = reverseString;
	}

	public int getAmount() 
	{
		return amount;
	}

	public void setAmount(final int amount) 
	{
		this.amount = amount;
	}
}
